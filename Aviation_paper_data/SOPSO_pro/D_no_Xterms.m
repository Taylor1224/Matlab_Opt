function y = D_no_Xterms(x)
    a = 5.76094e-3;
    a0 = [1.14249e-4,4.92119e-5,-1.78162e-5,6.9489e-5];
    
    B = [-8.5442e-7,0,0,0;
    0,1.08774e-6,0,0;
    0,0,-8.56403e-7,0;
    0,0,0,-2.796e-7];

    x = [x(1);x(2);x(3);x(4)];%这里的x(i)是得出的响应面模型里的X_scaled !!!
    y = a+a0*x+x'*B*x;% 要乘，不要点乘！！！
    
    % ！！！函数名必须与文件名相同！！！
    % ！！！函数定义时，function前面不能加任何注释（%）或者（%%）！！！
end