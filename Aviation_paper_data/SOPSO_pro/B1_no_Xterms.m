function y = B1_no_Xterms(x)
    a = 4.42446e-3;
    a0 = [4.05043e-5,6.94469e-5,-2.0622e-5,8.41049e-5];
    
    B = [1.39163e-6,0,0,0;
    0,7.07577e-7,0,0;
    0,0,4.41673e-7,0;
    0,0,0,8.77645e-8];

    c = [5.17798e-2,0,0,0;
        0,6.89986e-2,0,0;
        0,0,2.10794e-1,0;
        0,0,0,1.09157e-1];
    d = [5.26337e1;4.55018e1;4.84582e1;5.45754e1];

    x = [x(1);x(2);x(3);x(4)];%这里的x(i)是得出的响应面模型里的X_scaled !!!
    y = a+a0*(c*x-d)+(c*x-d)'*B*(c*x-d);% 要乘，不要点乘！！！
    
    % ！！！函数名必须与文件名相同！！！
    % ！！！函数定义时，function前面不能加任何注释（%）或者（%%）！！！
end