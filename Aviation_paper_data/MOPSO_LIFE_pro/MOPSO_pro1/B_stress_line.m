function y = B_stress_line(x)
    a = 7.14201e2;
    a0 = [6.49030,1.00723e1,-3.54744,1.28246e1];
    
    c = [4.91966e-2,0,0,0;
        0,7.57591e-2,0,0;
        0,0,2.17238e-1,0;
        0,0,0,1.00009e-1];

    d = [5.00036e1;5.00008e1;4.99648e1;5.00045e1];

    x = [x(1);x(2);x(3);x(4)];%这里的x(i)是得出的响应面模型里的X_scaled !!!
    y = a+a0*(c*x-d);% 要乘，不要点乘！！！
    
    % ！！！函数名必须与文件名相同！！！
    % ！！！函数定义时，function前面不能加任何注释（%）或者（%%）！！！
end