function y = D_have_Xterms(x)
    a = 5.75756e-3;
    a0 = [1.11913e-4,4.9273e-5,-1.73269e-5,6.8053e-5];
    
    B = [2.96654e-6,2.76201e-6/2,4.08971e-6/2,-6.9663e-6/2;
    2.76201e-6/2,-1.10657e-6,3.30878e-6/2,-4.04362e-6/2;
    4.08971e-6/2,3.30878e-6/2,-2.88362e-6,6.08983e-6/2;
    -6.9663e-6/2,-4.04362e-6/2,6.08983e-6/2,2.10611e-6];

    x = [x(1);x(2);x(3);x(4)];%这里的x(i)是得出的响应面模型里的X_scaled !!!
    y = a+a0*x+x'*B*x;

    % ！！！函数名必须与文件名相同！！！
    % ！！！函数定义时，function前面不能加任何注释（%）或者（%%）！！！
end