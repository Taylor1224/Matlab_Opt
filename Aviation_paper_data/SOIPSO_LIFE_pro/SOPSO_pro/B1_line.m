function y = B1_line(x)
    a = 4.42694e-3;
    a0 = [4.04294e-5,6.86644e-5,-2.0836e-5,8.36794e-5];
    
    c = [5.17798e-2,0,0,0;
        0,6.89986e-2,0,0;
        0,0,2.10794e-1,0;
        0,0,0,1.09157e-1];
    d = [5.26337e1;4.55018e1;4.84582e1;5.45754e1];

    x = [x(1);x(2);x(3);x(4)];%这里的x(i)是得出的响应面模型里的X_scaled !!!
    y = a+a0*(c*x-d);% 要乘，不要点乘！！！
    
    % ！！！函数名必须与文件名相同！！！
    % ！！！函数定义时，function前面不能加任何注释（%）或者（%%）！！！
end