function y = B_DOF_Xterms(x)

    c = [4.91966e-2,0,0,0;
        0,7.57591e-2,0,0;
        0,0,2.17238e-1,0;
        0,0,0,1.00009e-1];

    d = [5.00036e1;5.00008e1;4.99648e1;5.00045e1];

    x = [x(1);x(2);x(3);x(4)];

    % DOF_27387(叶片径向变形)的目标函数：
    a1 = 9.90567e-1;

    b1 = [8.53132e-3,6.16370e-3,3.89536e-3,1.16272e-2];
    
    B1 = [9.60864e-5,4.62971e-6/2,6.97340e-6/2,-1.64547e-5/2;
        4.62971e-6/2,2.32914e-5,-6.11107e-7/2,-6.30436e-6/2;
        6.97340e-6/2,-6.11107e-7/2,3.62693e-5,5.49038e-5/2;
        -1.64547e-5/2,-6.30436e-6/2,5.49038e-5/2,5.17507e-5];

    y = a1+b1*(c*x-d)+(c*x-d)'*B1*(c*x-d);




end